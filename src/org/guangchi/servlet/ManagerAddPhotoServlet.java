@WebServlet("/ManagerAddPhotoServlet")
@MultipartConfig
public class ManagerAddPhotoServlet extends HttpServlet {
	
    private static final long serialVersionUID = 1L;
    // 上传文件存储目录
    private static final String UPLOAD_DIRECTORY = "upload";
    // 上传配置
    private static final int MEMORY_THRESHOLD   = 1024 * 1024 * 3;  // 3MB
    private static final int MAX_FILE_SIZE      = 1024 * 1024 * 40; // 40MB
    private static final int MAX_REQUEST_SIZE   = 1024 * 1024 * 50; // 50MB
	
	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		doPost(request, response);
		
	}
 
	protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		System.out.println("0");
		  // 检测是否为多媒体上传
    if (!ServletFileUpload.isMultipartContent(request)) {
        // 如果不是则停止
        PrintWriter writer = response.getWriter();
        writer.println("Error: 表单必须包含 enctype=multipart/form-data");
        writer.flush();
        return;
    }

    
    System.out.println("0");
    
    // 配置上传参数
    DiskFileItemFactory factory = new DiskFileItemFactory();
    // 设置内存临界值 - 超过后将产生临时文件并存储于临时目录中
    factory.setSizeThreshold(MEMORY_THRESHOLD);
    // 设置临时存储目录
    factory.setRepository(new File(System.getProperty("java.io.tmpdir")));

    ServletFileUpload upload = new ServletFileUpload(factory);
     
    // 设置最大文件上传值
    upload.setFileSizeMax(MAX_FILE_SIZE);
     
    // 设置最大请求值 (包含文件和表单数据)
    upload.setSizeMax(MAX_REQUEST_SIZE);
    
    // 中文处理
    upload.setHeaderEncoding("UTF-8"); 

    // 构造临时路径来存储上传的文件
    // 这个路径相对当前应用的目录

   String uploadPath = " img\\food_img";
     
   
   
    // 如果目录不存在则创建
    File uploadDir = new File(uploadPath);
    if (!uploadDir.exists()) {
        uploadDir.mkdir();
    }
    
    System.out.println("0");
    try {
        // 解析请求的内容提取文件数据
        @SuppressWarnings("unchecked")
        List<FileItem> formItems = upload.parseRequest(request);

        if (formItems != null && formItems.size() > 0) {
            // 迭代表单数据
            for (FileItem item : formItems) {
                // 处理不在表单中的字段
                if (!item.isFormField()) {
                    String fileName = new File(item.getName()).getName();
                    String filePath = uploadPath + File.separator + fileName;
                    File storeFile = new File(filePath);
                   
                    // 通过文件的原始名称，对上传文件类型做限制，只能上传jpg图片文件
                    if (filePath.endsWith("jpg") ) {
                    // 在控制台输出文件的上传路径
                        System.out.println(filePath);
                    	// 保存文件到硬盘
                        item.write(storeFile);
                        System.out.println("0");
                        String Newpath="/Guangchi/img/food_img/dfms/"+filePath.substring(uploadPath.length()+1);
                        System.out.println(Newpath);
                		request.setCharacterEncoding("utf-8" );
                		NewFood food=new NewFood(Newpath);
                		ManagerAddFoodDao.AddFood(food);
                       
                		System.out.println("0");
                        
                        
                        
                        
                        request.setAttribute("message",
                            "上传成功!");
                  
                    }
                    else {
                    	request.setAttribute("message",
                                "不支持这种文件!");
                    };
                }
            }
        }
    } catch (Exception ex) {
        request.setAttribute("message",
                "错误信息: " + ex.getMessage());
    }
    // 跳转到 message.jsp
    getServletContext().getRequestDispatcher("/message.jsp").forward(
            request, response);
		
		
		


    }
		
}	
		


